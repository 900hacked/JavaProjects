package myFirstClass;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i = 0;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}
		
		// do while loop
		
		int a = 0;
		do {
			System.out.println(a);
			a++;
		}
		while (a < 5);
		
		// i will go to heaven 20 times
		
		int b = 0;
		while (b < 20) {
			System.out.println("I will go to heaven");
			b++;
		}
		
		int num = 5;
		while (num <= 10) {
			System.out.println(num);
			num++;
		}
		
		int x = 10;
		while (x >= 0) {
			System.out.println(x);
			x--;
		}
		
		// For loop
		
		for (int z = 5; z <= 20; z++) {
			System.out.println(z);
		}
		
		// i am dorian 10 times
		
		for (int name = 0; name < 10; name++) {
			System.out.println("I am Dorian");
		}

	}

}

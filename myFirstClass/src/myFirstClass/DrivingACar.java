package myFirstClass;

public class DrivingACar {
	public static void StartTheCar() {
		System.out.println("put your foot on the brake pedal");
		System.out.println("put the key in the key hole");
		System.out.println("turn the key");
	}
	public static void MoveTheCar() {
		System.out.println("put the car into the drive gear");
		System.out.println("put your foot on the accelerator");
		System.out.println("Move the car");
	}

	public static void main(String[] args) {
		StartTheCar();
		MoveTheCar();

	}

}
